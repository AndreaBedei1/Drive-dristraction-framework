# Reconstruct HMM from artifact
# ------------------------------------------------------------------------------
def reconstruct_hmm(artifact: dict):
    hmm_dict = artifact["hmm"]
    n_states = hmm_dict["n_states"]
    cov_arr  = np.array(hmm_dict["covars"])
    if cov_arr.ndim == 1:
        cov_type = "spherical"
    elif cov_arr.ndim == 2:
        cov_type = "diag" if cov_arr.shape[0] == n_states else "tied"
    elif cov_arr.ndim == 3:
        cov_type = "full"
    else:
        cov_type = hmm_dict.get("covariance_type", "diag")

    hmm_model = GaussianHMM(n_components=n_states, covariance_type=cov_type)
    hmm_model.startprob_ = np.array(hmm_dict["startprob"])
    hmm_model.transmat_  = np.array(hmm_dict["transmat"])
    hmm_model.means_     = np.array(hmm_dict["means"])
    hmm_model.covars_    = cov_arr
    return hmm_model, n_states

# ------------------------------------------------------------------------------
# Generate test data for a given artifact (using its own module)
# ------------------------------------------------------------------------------
def generate_test_data(artifact: dict, module, data_path: str, seed: int):
    d_raw, e_raw, eb_raw, db_raw = module.load_data(data_path)
    module.run_integrity_checks(d_raw, e_raw)

    users = sorted(set(d_raw["user_id"].unique()) | set(e_raw["user_id"].unique()))
    train_users, cal_users, test_users = module.split_users(users, seed=seed)

    impute_stats = module.fit_train_imputation_stats(train_users, d_raw)
    d = module.apply_imputation_stats(d_raw, impute_stats)

    le_pred, le_emo = module.fit_label_encoders(train_users, d, e_raw)
    pred_enc = module.build_encoding_map(le_pred)
    emo_enc  = module.build_encoding_map(le_emo)
    baselines_bl = module.build_baselines(train_users, eb_raw, db_raw)

    d_train = d[d["user_id"].isin(train_users)]
    gmp = float(d_train[["model_prob_start", "model_prob_end"]].stack().median())
    gep = float(d_train[["emotion_prob_start", "emotion_prob_end"]].stack().median())
    if not np.isfinite(gmp): gmp = 0.5
    if not np.isfinite(gep): gep = 0.5

    wbs, webs, errs = module.build_lookups(d, e_raw, users)

    H = artifact["config"]["H"]
    T = artifact["config"]["T"]

    df_te = module.generate_samples(
        H, T, test_users, wbs, webs, errs,
        baselines_bl, pred_enc, emo_enc, gmp, gep
    )
    df_te = module.add_causal_session_features(df_te)
    df_te = module.apply_feature_postprocess(df_te, artifact["postprocess"])
    df_te = module.sort_for_hmm(df_te)

    target_t1 = build_t1_target(df_te, errs)

    return df_te, target_t1, errs, test_users

# ------------------------------------------------------------------------------
# Compute construct‑validity metrics for a given score and DataFrame
# ------------------------------------------------------------------------------
def compute_construct_metrics(score: np.ndarray, df: pd.DataFrame,
                              target: np.ndarray, name: str) -> dict:
    """
    Compute C‑index, Kendall's τ, onset discrimination, and recovery curve
    parameters. Returns a dictionary of metrics.
    """
    LOG.info("Computing construct‑validity metrics for %s", name)

    # C‑index and Kendall τ
    cidx_res = compute_cindex(score, df, seed=42)   # uses default n_sample_pairs
    c_index = cidx_res["c_index"]
    kendall_tau = cidx_res["kendall_tau"]

    # Onset discrimination (during vs baseline effect size)
    onset_res = compute_onset_discrimination(df, score, window_s=5.0)
    onset_effect = onset_res.get("during_vs_baseline", {}).get("effect_r", float("nan"))

    # Recovery curves
    fits_df, rec_summary = fit_recovery_curves(df, score, min_points=6)
    rec_tau_median = rec_summary.get("tau_median", float("nan"))
    rec_r2_median = rec_summary.get("r2_median", float("nan"))
    n_fits = rec_summary.get("n_events_good_fit", 0)

    return {
        "c_index": round(c_index, 4),
        "kendall_tau": round(kendall_tau, 4),
        "onset_effect_r": round(onset_effect, 4),
        "recovery_tau_median": round(rec_tau_median, 2) if not np.isnan(rec_tau_median) else None,
        "recovery_r2_median": round(rec_r2_median, 3) if not np.isnan(rec_r2_median) else None,
        "recovery_n_events": n_fits,
    }

# ------------------------------------------------------------------------------
# Main comparison
# ------------------------------------------------------------------------------
def run(args):
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    # ---- Load no‑time artifact ----
    LOG.info("Loading no‑time artifact from %s", args.artifact_notime)
    art_notime = joblib.load(args.artifact_notime)

    # ---- Reconstruct HMM ----
    hmm_notime, n_states_notime = reconstruct_hmm(art_notime)

    # ---- Generate test data for no‑time HMM ----
    LOG.info("Generating test data for no‑time HMM...")
    df_te_notime, target_notime, errs_notime, test_users_notime = generate_test_data(
        art_notime, notime_module, args.data_path, args.seed
    )

    target_te = target_notime
    LOG.info("Test set positive rate: %.2f%% (%d samples)",
             100 * target_te.mean(), len(target_te))

    # ---- Compute FTD scores for no‑time HMM ----
    scaler_notime = art_notime["scaler"]
    feat_cols_notime = art_notime["feature_cols"]
    X_te_notime = scaler_notime.transform(df_te_notime[feat_cols_notime].astype(float))
    b_te_notime = notime_module.sequence_bounds(df_te_notime)

    gamma_te_notime = np.zeros((len(df_te_notime), n_states_notime))
    for s, e in b_te_notime:
        if e > s:
            gamma_te_notime[s:e] = hmm_notime.predict_proba(X_te_notime[s:e])

    risk_order_notime = np.array(art_notime["state_profile"]["risk_order"])
    weights_notime = np.array(art_notime["risk_weights"])
    intercept_notime = art_notime["risk_intercept"]
    ftd_notime = notime_module.compute_risk_score(
        gamma_te_notime, risk_order_notime, weights_notime, intercept_notime
    )

    # ---- Time‑only baseline (fitted on calibration set) ----
    LOG.info("Generating calibration data for time‑only LR...")
    users_all = sorted(set(notime_module.load_data(args.data_path)[0]["user_id"].unique()) |
                       set(notime_module.load_data(args.data_path)[1]["user_id"].unique()))
    _, cal_users, _ = notime_module.split_users(users_all, seed=args.seed)

    def get_samples_for_users(module, user_set):
        d_raw, e_raw, eb_raw, db_raw = module.load_data(args.data_path)
        module.run_integrity_checks(d_raw, e_raw)
        users_all = sorted(set(d_raw["user_id"].unique()) | set(e_raw["user_id"].unique()))
        train_u, _, _ = module.split_users(users_all, seed=args.seed)

        impute_stats = module.fit_train_imputation_stats(train_u, d_raw)
        d = module.apply_imputation_stats(d_raw, impute_stats)

        le_pred, le_emo = module.fit_label_encoders(train_u, d, e_raw)
        pred_enc = module.build_encoding_map(le_pred)
        emo_enc  = module.build_encoding_map(le_emo)
        baselines_bl = module.build_baselines(train_u, eb_raw, db_raw)

        d_train = d[d["user_id"].isin(train_u)]
        gmp = float(d_train[["model_prob_start", "model_prob_end"]].stack().median())
        gep = float(d_train[["emotion_prob_start", "emotion_prob_end"]].stack().median())
        if not np.isfinite(gmp): gmp = 0.5
        if not np.isfinite(gep): gep = 0.5

        wbs, webs, errs = module.build_lookups(d, e_raw, users_all)

        H = art_notime["config"]["H"]
        T = art_notime["config"]["T"]

        df = module.generate_samples(
            H, T, user_set, wbs, webs, errs,
            baselines_bl, pred_enc, emo_enc, gmp, gep
        )
        df = module.add_causal_session_features(df)
        df = module.apply_feature_postprocess(df, art_notime["postprocess"])
        return module.sort_for_hmm(df), errs

    LOG.info("Generating calibration set...")
    df_ca, errs_ca = get_samples_for_users(notime_module, cal_users)
    target_ca = build_t1_target(df_ca, errs_ca)
    LOG.info("Calibration positive rate: %.2f%% (%d samples)",
             100 * target_ca.mean(), len(target_ca))

    time_ca = df_ca["time_since_distraction_end"].fillna(0.0).values.reshape(-1, 1)
    time_te = df_te_notime["time_since_distraction_end"].fillna(0.0).values.reshape(-1, 1)

    lr_time = fit_lr_baseline(time_ca, target_ca, seed=args.seed)
    lr_proba_te = lr_time.predict_proba(time_te)[:, 1]

    # ---- Collect predictive metrics (keep proba & df for later) ----
    models = [
        {**score_model("HMM (no-time)", ftd_notime, target_te), "proba": ftd_notime, "df": df_te_notime},
        {**score_model("Time-only LR", lr_proba_te, target_te), "proba": lr_proba_te, "df": df_te_notime},
    ]

    # ---- Add construct‑validity metrics (still have proba & df) ----
    for m in models:
        m.update(compute_construct_metrics(m["proba"], m["df"], target_te, m["name"]))

    # ---- Generate plots (still need proba) ----
    plot_roc(models, target_te, out_dir / "roc_curves.png")
    plot_pr(models, target_te, out_dir / "pr_curves.png")
    plot_calibration_curves(models, target_te, out_dir / "calibration_curves.png")
    plot_delta_bar(models, out_dir / "delta_bar.png")

    # ---- Remove bulky DataFrames and proba before saving table ----
    for m in models:
        del m["df"]
        del m["proba"]

    # ---- Compute delta columns relative to time‑only LR for predictive metrics only ----
    b0_auc = next(m for m in models if m["name"] == "Time-only LR")["auc_roc"]
    b0_ap  = next(m for m in models if m["name"] == "Time-only LR")["avg_precision"]
    for m in models:
        m["delta_auc_vs_time"] = round(m["auc_roc"] - b0_auc, 4)
        m["delta_ap_vs_time"]  = round(m["avg_precision"] - b0_ap, 4)

    with open(out_dir / "full_comparison.json", "w") as f:
        json.dump(models, f, indent=2)

    # Pretty print
    LOG.info("─── Full Comparison ─────────────────────────────────────────")
    header = (f"{'Model':<22}  {'AUC-ROC':>8}  {'Avg-P':>8}  {'Cal-r':>7}  "
              f"{'C-idx':>7}  {'Kendall τ':>10}  {'Onset r':>8}  {'τ_recov':>8}  {'R²_rec':>7}")
    LOG.info(header)
    for m in models:
        LOG.info(
            "%-22s  %8.4f  %8.4f  %7.4f  %7.4f  %10.4f  %8.4f  %8.2f  %7.3f",
            m["name"], m["auc_roc"], m["avg_precision"], m["calibration_r"],
            m["c_index"], m["kendall_tau"], m["onset_effect_r"],
            m["recovery_tau_median"] or 0, m["recovery_r2_median"] or 0,
        )

    LOG.info("All outputs saved to %s", out_dir)

# ------------------------------------------------------------------------------
# CLI
# ------------------------------------------------------------------------------
if __name__ == "__main__":
    p = argparse.ArgumentParser(
        description="Full evaluation: HMM vs time‑only LR including construct validity"
    )
    p.add_argument("--artifact-notime", default="result_no_time/impairment_hmm.joblib",
                   help="Path to the no‑time HMM artifact")
    p.add_argument("--data-path", default="data", help="Path to data directory")
    p.add_argument("--output-dir", default="full_comparison/",
                   help="Directory to save outputs")
    p.add_argument("--seed", type=int, default=42, help="Random seed")
    args = p.parse_args()
    run(args)