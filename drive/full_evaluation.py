#!/usr/bin/env python3
"""
full_evaluation.py
==================
Comprehensive comparison of HMM‑based FTD score against a simple
time‑only logistic regression baseline. In addition to predictive metrics
(AUC‑ROC, Average Precision, calibration), this script computes construct‑validity
metrics:

  - C‑index (concordance with known causal ordering)
  - Kendall's τ between time since distraction and FTD (negative = impairment decays)
  - Onset discrimination effect size (Mann–Whitney r)
  - Recovery curve parameters (τ, R²) from exponential fits

All models are evaluated on the same test users, using a T=1 lookahead target.
The time‑only baseline is a logistic regression fitted on the calibration set
using only the `time_since_distraction_end` feature.

Outputs
-------
- JSON table with all metrics per model
- ROC, PR, calibration, and delta bar plots (as before)
- Optional: construct‑validity summary plots for each model (recovery curves,
  onset discrimination) can be generated by setting `--plot-construct`
"""

